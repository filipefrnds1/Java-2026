public class ProductAula97 {

        private String name;
        private double price;

        public ProductAula97(){

        }

        public ProductAula97(String name, double price){
            this.name = name;
            this.price = price;
        }

        public String getName(){
            return name;
        }

        public void setName(String name){
            this.name = name;
        }

        public double getPrice(){
            return price;
        }

        public void setPrice(double price){
            this.price = price;
        }




}
